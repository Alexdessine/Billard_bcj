                <p class="list mt-4 mb-2">Le nouveau millénaire</p>

                <p class="mb-1">En 2010, un nouveau projet sportif voit le jour visant à amener les jeunes talents du club à l'échelle internationale. Le club renforce sa structure et sa communication pour continuer à attirer divers publics. Malgré la pandémie de Covid-19, le BCJ fait preuve de résilience et continue de se développer, en accueillant le Ripault Olympique Club et en remportant la Coupe de France par équipes, couronnant ainsi son palmarès.</p>

                <p class="mb-2">La légende du BCJ est une illustration vibrante de la passion et de la résilience humaine. Elle nous rappelle que le succès n'est pas un droit acquis, mais le fruit d'un travail acharné, de dévouement et d'engagement.</p>
