                <h6 class="text-md">La saga du Billard Club de Joué-Lès-Tours (BCJ) : un voyage à travers les décennies</h6>
                <p class="mt-4">Plongez-vous dans l’intrigante histoire du Billard Club Jocondien, né dans une simple arrière-salle d’un bar à Joué-Lès-Tours en 1977. La particularité du nom ? Il tire son essence de la dénomination romaine de la ville, "Jocondiacum", représentative de ses habitants, les Jocondiens. En 2014, le club se rebaptise en Billard Club de Joué-Lès-Tours, embrassant ainsi pleinement ses origines.</p>

                <p class="list mt-4 mb-2">Le début des aventures</p>

                <p class="mb-4">A l'aube de sa création, le BCJ ne comptait que cinq billards français et un groupe d'amis passionnés de billard. Les années 70 et 80 marquent l'apogée du club avec ses premiers titres de champion de France en catégorie Excellence, notamment grâce à la virtuosité de Serge Tessier. Cependant, les années suivantes seront marquées par des périodes de difficultés dues aux humeurs changeantes des propriétaires.</p>
