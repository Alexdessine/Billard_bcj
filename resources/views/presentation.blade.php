                <p class="list mt-4 mb-2">La renaissance du Phénix</p>

                <p class="mb-4">L'année 1991 signe la résurgence du club. Eric Leroux, ancien joueur et dirigeant départemental des années 80, prend les rênes de ce club en déclin, déterminé à insuffler une nouvelle vie à l'institution. Avec une vision claire et des convictions solides, il met en place un plan stratégique audacieux pour attirer partenaires, joueurs potentiels et publics variés, et promouvoir le billard à poches, une discipline jusqu'alors inexistante dans la région.</p>

                <p class="list mb-2">Les années 90 : Transition et expansion</p>

                <p class="mb-2">Grâce à cette révolution, les effectifs du BCJ s'étoffent rapidement. Mais les défis continuent de surgir et le club est expulsé de ses locaux en 1996. Pendant deux ans, il mène une existence fragmentée entre deux clubs. 1997 signe le renouveau du BCJ qui parvient à organiser son premier événement majeur : le championnat de France FFB de 8 Pool. Grâce au soutien de la mairie de Joué-Lès-Tours, il s'installe dans un hangar transformé en salle de billard de 250 m² en 1998.</p>
